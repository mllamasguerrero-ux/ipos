using System;
using System.Collections.Generic;
using System.Collections.ObjectModel;
using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IposV3.Model
{




    public class Empresa
    {


        public Empresa()
        {
            Creado = DateTime.UtcNow;
            Modificado = DateTime.UtcNow;

            Activo = BoolCS.S;
            Clave = "";
            Nombre = "";
        }

        [Key]
        [DatabaseGenerated(DatabaseGeneratedOption.Identity)]//switch on autogenerated
        public long Id { get; set; }


        [Column(TypeName = "char(1)")]
        public BoolCS Activo { get; set; }


        public DateTimeOffset Creado { get; set; }

        public DateTimeOffset Modificado { get; set; }

        [StringLength(Domains.ClaveLength)]
        public string Clave { get; set; }

        [StringLength(Domains.Stdtext_mediumLength)]
        public string Nombre { get; set; }

        [StringLength(Domains.DescripcionLength)]
        public string? Descripcion { get; set; }

        [StringLength(Domains.Stdtext_mediumLength)]
        public string? Rfc { get; set; }


        [StringLength(Domains.Stdtext_shortLength)]
        public string? Telefono { get; set; }

        [StringLength(Domains.Stdtext_shortLength)]
        public string? Fax { get; set; }


        [StringLength(Domains.Stdtext_mediumLength)]
        public string? Correoe { get; set; }

        [StringLength(Domains.Stdtext_mediumLength)]
        public string? Paginaweb { get; set; }



        public virtual ICollection<Sucursal>
            Sucursales
        { get; private set; } =
            new ObservableCollection<Sucursal>();


    }
}
