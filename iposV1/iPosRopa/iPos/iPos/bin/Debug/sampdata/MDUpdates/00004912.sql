EXECUTE block
AS                        
declare variable SUCURSALID d_fk;
declare variable SUCURSALCLAVE d_CLAVE_NULL;
BEGIN

    SELECT FIRST 1 SUCURSALID FROM PARAMETRO INTO :SUCURSALID;
    SELECT FIRST 1 CLAVE FROM SUCURSAL WHERE ID = :SUCURSALID INTO :SUCURSALCLAVE;
    UPDATE PARAMETRO SET PARAMETRO.sucursalnumero = :SUCURSALCLAVE;

END
