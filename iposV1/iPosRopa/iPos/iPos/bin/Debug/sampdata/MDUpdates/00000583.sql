CREATE OR ALTER VIEW GET_PEDIDO(
    DOCTOID,
    PRODUCTO,
    DESCRIPCION1,
    DESCRIPCION2,
    CANTIDAD,
    FECHA,
    HORA)
AS
SELECT
  PED.ID, 
  PROD.CLAVE,
  PROD.DESCRIPCION1,
  PROD.DESCRIPCION2,
  PEDDET.CANTIDAD,
  PED.FECHA,
  CAST(PED.FECHAHORA AS TIME) AS HORA

  FROM MOVTO PEDDET
    LEFT JOIN DOCTO PED
      ON PED.ID = PEDDET.DOCTOID
    LEFT JOIN PRODUCTO PROD
      ON PROD.ID = PEDDET.PRODUCTOID
  WHERE PED.TIPODOCTOID = 14
    AND PED.ESTATUSDOCTOPEDIDOID = 0
;