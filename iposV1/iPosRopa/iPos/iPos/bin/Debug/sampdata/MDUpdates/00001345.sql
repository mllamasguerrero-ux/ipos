CREATE OR ALTER TRIGGER PRODUCTO_BUIMPUESTO FOR PRODUCTO
ACTIVE BEFORE UPDATE POSITION 0
AS
begin
  IF (NEW.tasaiva <> OLD.TASAIVA or NEW.tasaieps <> OLD.TASAIEPS ) THEN
  BEGIN
      NEW.TASAIMPUESTO = COALESCE(NEW.TASAIEPS,0) + COALESCE(NEW.TASAIVA,0) + (COALESCE(NEW.TASAIEPS,0) *  COALESCE(NEW.TASAIVA,0))/100;
  END


end