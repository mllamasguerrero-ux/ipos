create or alter procedure QUOTA_UPDATE_ANIO (
    VENDEDORID D_FK,
    ANIO integer,
    QUOTA_ENERO D_PRECIO,
    QUOTA_FEBRERO D_PRECIO,
    QUOTA_MARZO D_PRECIO,
    QUOTA_ABRIL D_PRECIO,
    QUOTA_MAYO D_PRECIO,
    QUOTA_JUNIO D_PRECIO,
    QUOTA_JULIO D_PRECIO,
    QUOTA_AGOSTO D_PRECIO,
    QUOTA_SEPTIEMBRE D_PRECIO,
    QUOTA_OCTUBRE D_PRECIO,
    QUOTA_NOVIEMBRE D_PRECIO,
    QUOTA_DICIEMBRE D_PRECIO)
returns (
    ERRORCODE type of D_ERRORCODE)
as
BEGIN

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,1,:QUOTA_ENERO) INTO :ERRORCODE;
   IF(:ERRORCODE <> 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,2,:QUOTA_FEBRERO) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END 

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,3,:QUOTA_MARZO) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END     

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,4,:QUOTA_ABRIL) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END   

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,5, :QUOTA_MAYO) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END      

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,6, :QUOTA_JUNIO) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END     

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,7, :QUOTA_JULIO) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END   

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,8, :QUOTA_AGOSTO) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END 

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,9, :QUOTA_SEPTIEMBRE) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END    

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,10, :QUOTA_OCTUBRE) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END     

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,11, :QUOTA_NOVIEMBRE) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END  

   SELECT ERRORCODE FROM QUOTA_UPDATE(:VENDEDORID, :ANIO,12, :QUOTA_DICIEMBRE) INTO :ERRORCODE;
   IF(:ERRORCODE = 0) then
   BEGIN
        SUSPEND;
        EXIT;
   END






   SUSPEND;

   
    WHEN ANY DO
    BEGIN
        ERRORCODE = 1001;
        SUSPEND;
    END

END