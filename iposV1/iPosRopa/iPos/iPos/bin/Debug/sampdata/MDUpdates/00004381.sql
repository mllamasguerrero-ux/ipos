CREATE OR ALTER VIEW ALMACEN_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    DESCRIPCION)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBRE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    DESCRIPCION

  FROM ALMACEN
  ORDER BY CLAVE
;
