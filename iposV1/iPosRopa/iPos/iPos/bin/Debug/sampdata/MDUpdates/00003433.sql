CREATE OR ALTER VIEW TERMINALPAGOSERVICIO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    TERMINAL,
    SUCURSALCLAVE,
    SUCURSALID,
    CONSECUTIVO)
AS
SELECT
    ID,
    COALESCE(TRIM(TERMINAL), '') ||  COALESCE(TRIM(SUCURSALCLAVE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    TERMINAL,
    SUCURSALCLAVE,
    SUCURSALID,
    CONSECUTIVO

  FROM TERMINALPAGOSERVICIO
  ORDER BY TERMINAL
;

