create or alter procedure CORTERECOLECCION_CORTEINSERTAR (
    CORTERECOLECCIONID type of D_FK,
    CORTEID type of D_FK)
returns (
    ERRORCODE type of D_ERRORCODE)
as
BEGIN

  ERRORCODE = 0;

    UPDATE CORTE SET
    CORTERECOLECCIONID = :CORTERECOLECCIONID
    WHERE ID = :CORTEID;


    SELECT ERRORCODE
    FROM CORTERECOLECCION_MONTOUPDATE( :CORTERECOLECCIONID)
    INTO :ERRORCODE;


   SUSPEND;
END