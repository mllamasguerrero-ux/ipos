CREATE OR ALTER VIEW CLIENTE_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    TIPOPERSONAID,
    CLAVE,
    NOMBRE,
    DESCRIPCION,
    MEMO,
    SALUDOID,
    NOMBRES,
    APELLIDOS,
    RAZONSOCIAL,
    RFC,
    CONTACTO1,
    CONTACTO2,
    USERNAME,
    CLAVEACCESO,
    DOMICILIO,
    COLONIA,
    CIUDAD,
    MUNICIPIO,
    ESTADO,
    PAIS,
    CODIGOPOSTAL,
    TELEFONO1,
    TELEFONO2,
    TELEFONO3,
    CELULAR,
    NEXTEL,
    EMAIL1,
    EMAIL2,
    EMPRESAID,
    VENDEDORID,
    ESCLIENTE,
    ESCLIENTEGENERAL,
    ESPROVEEDOR,
    ESUSUARIO,
    LISTAPRECIOID,
    VIGENCIA,
    RESET_PASS)
AS
select
    id, 

    COALESCE(TRIM(CLAVE), '')
    ||COALESCE(TRIM(NOMBRE), '')
    ||COALESCE(TRIM(NOMBRES), '')
    ||COALESCE(TRIM(APELLIDOS),'')
    ||COALESCE(TRIM(RAZONSOCIAL), '')
    ||COALESCE(TRIM(RFC), '')
    ||COALESCE(TRIM(DOMICILIO), '') BUSQUEDASTRING,

    activo,
    creado,
    creadopor_userid,
    modificado,
    modificadopor_userid,
    tipopersonaid,
    clave,
    nombre,
    descripcion,
    memo,
    saludoid,
    nombres,
    apellidos,
    razonsocial,
    rfc,
    contacto1,
    contacto2,
    username,
    claveacceso,
    domicilio,
    colonia,
    ciudad,
    municipio,
    estado,
    pais,
    codigopostal,
    telefono1,
    telefono2,
    telefono3,
    celular,
    nextel,
    email1,
    email2,
    empresaid,
    vendedorid,
    escliente,
    esclientegeneral,
    esproveedor,
    esusuario,
    listaprecioid,
    vigencia,
    reset_pass
from persona
where tipopersonaid = 50
order by clave
;