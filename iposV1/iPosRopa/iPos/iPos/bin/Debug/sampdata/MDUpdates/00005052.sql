CREATE OR ALTER VIEW CUENTABANCO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    NOMBREBANCO,
    NOMBRE,
    RFC,
    NOCUENTA,
    BANCOID)
AS
SELECT
    CUENTABANCO.ID,
    COALESCE(TRIM(CUENTABANCO.RFC), '') ||  COALESCE(TRIM(CUENTABANCO.NOMBRE), '')  ||  COALESCE(TRIM(CUENTABANCO.NOCUENTA), '')  BUSQUEDASTRING,
    CUENTABANCO.ACTIVO,
    CUENTABANCO.CREADO,
    CUENTABANCO.CREADOPOR_USERID,
    CUENTABANCO.MODIFICADO,
    CUENTABANCO.MODIFICADOPOR_USERID,
    BANCOS.nombre NOMBREBANCO,
    CUENTABANCO.NOMBRE,
    CUENTABANCO.RFC,
    CUENTABANCO.NOCUENTA ,
    CUENTABANCO.BANCOID

  FROM CUENTABANCO
  LEFT JOIN BANCOS ON BANCOS.ID = CUENTABANCO.BANCOID
  ORDER BY NOMBRE
;