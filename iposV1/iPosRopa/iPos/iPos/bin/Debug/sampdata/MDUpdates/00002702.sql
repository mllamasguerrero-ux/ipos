create or alter procedure CONSOLIDADO_ESDEVOLUCIONCONS (
    DOCTOID D_FK)
returns (
    ESDEVOLUCIONCONS D_BOOLCN,
    ERRORCODE D_ERRORCODE)
as
declare variable DOCTODEVCONSID D_FK;
BEGIN



   SELECT DOCTO.devconsid FROM DOCTO
   WHERE ID = :DOCTOID
   INTO :DOCTODEVCONSID;

       
   IF(COALESCE(:DOCTODEVCONSID,0) <> 0) THEN
   BEGIN
    ESDEVOLUCIONCONS = 'S';
   END
   ELSE
   BEGIN
     ESDEVOLUCIONCONS = 'N';
   END


   ERRORCODE = 0;
   SUSPEND;


   WHEN ANY DO
    BEGIN
        ERRORCODE = 1009;
        SUSPEND;
    END
END