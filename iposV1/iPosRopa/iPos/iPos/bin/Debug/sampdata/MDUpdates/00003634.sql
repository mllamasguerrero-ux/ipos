CREATE OR ALTER VIEW TIPODIFERENCIAINVENTARIO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    DESCRIPCION)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBRE), '')  ||  COALESCE(TRIM(DESCRIPCION), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    DESCRIPCION

  FROM TIPODIFERENCIAINVENTARIO
  ORDER BY CLAVE
;