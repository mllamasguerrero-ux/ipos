CREATE OR ALTER VIEW CAJA_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    DESCRIPCION,
    NOMBREIMPRESORA,
    TERMINAL)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBRE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    DESCRIPCION,
    NOMBREIMPRESORA,
    TERMINAL

  FROM CAJA
  ORDER BY CLAVE
;