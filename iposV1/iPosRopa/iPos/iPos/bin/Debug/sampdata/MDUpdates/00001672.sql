CREATE OR ALTER VIEW MEDICO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    NOMBRE,
    CEDULA,
    MEDICOPROPIO)
AS
SELECT
    ID,
    COALESCE(TRIM(CEDULA), '') ||  COALESCE(TRIM(NOMBRE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    NOMBRE ,
    CEDULA,
    MEDICOPROPIO

  FROM MEDICO
  ORDER BY NOMBRE
;