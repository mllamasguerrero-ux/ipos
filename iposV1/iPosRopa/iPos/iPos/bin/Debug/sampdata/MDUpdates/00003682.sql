CREATE OR ALTER VIEW MERCHANTPAGOSERVICIO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    MERCHANTID,
    SUCURSALCLAVE)
AS
SELECT
    ID,
    COALESCE(TRIM(MERCHANTID), '') ||  COALESCE(TRIM(SUCURSALCLAVE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    MERCHANTID,
    SUCURSALCLAVE

  FROM MERCHANTPAGOSERVICIO
  ORDER BY MERCHANTID
;