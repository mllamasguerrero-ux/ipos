CREATE OR ALTER VIEW SUBTIPOCLIENTE_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBRE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE

  FROM SUBTIPOCLIENTE
  ORDER BY CLAVE
;