create or alter procedure PRE_INVENTARIO_INICIAL
returns (
    ERRORCODE D_ERRORCODE)
as
  DECLARE VARIABLE NAMESEQ VARCHAR(64);
  DECLARE VARIABLE COMM VARCHAR(64);
BEGIN


   DELETE FROM KARDEX;
   DELETE FROM MOVTO;
   DELETE FROM DOCTOPAGO;
   DELETE FROM DOCTO;

   DELETE FROM IMP_FILES;
   DELETE FROM EXP_FILES;

   DELETE FROM INVENTARIO;

   FOR SELECT SEQUENCENAME
   FROM FOLIO
   INTO :NAMESEQ DO
   BEGIN
      COMM = 'ALTER SEQUENCE ' || :NAMESEQ || ' RESTART WITH 0';

      EXECUTE STATEMENT :COMM;
   END


   ERRORCODE = 0;
   SUSPEND;

   WHEN ANY DO
   BEGIN
      ERRORCODE = 1016;
      SUSPEND;
   END 
END