CREATE OR ALTER TRIGGER CONTRARECIBODTL_INSTOTALES FOR CONTRARECIBODTL
ACTIVE AFTER INSERT POSITION 0
AS      
   DECLARE VARIABLE ERRORCODE D_ERRORCODE;
begin
  /* Trigger text */
  
   IF ( 0 <> COALESCE(NEW.TOTAL,0) or
        0 <> COALESCE(NEW.SALDO,0) or
        0 <> COALESCE(NEW.ABONO,0)
      ) THEN
    BEGIN
         SELECT ERRORCODE
         FROM CONTRARECIBO_ACUMULAR_TOTALES( NEW.CONTRARECIBOID ) INTO :ERRORCODE;
    END
end