CREATE OR ALTER VIEW REPORTE_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    NOMBRE,
    ARCHIVO,
    DESCRIPCION)
AS
SELECT
    ID,
    COALESCE(TRIM(NOMBRE), '') ||  COALESCE(TRIM(ARCHIVO), '')  ||  COALESCE(TRIM(DESCRIPCION), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    NOMBRE,
    ARCHIVO,
    DESCRIPCION

  FROM REPORTES
  ORDER BY ID
;