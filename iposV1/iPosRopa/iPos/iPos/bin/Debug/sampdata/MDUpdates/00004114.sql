CREATE OR ALTER VIEW AREA_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBREAREA), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBREAREA AS NOMBRE

  FROM AREA  
  ORDER BY CLAVE
;