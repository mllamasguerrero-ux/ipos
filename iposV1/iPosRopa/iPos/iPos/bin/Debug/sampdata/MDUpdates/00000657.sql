CREATE OR ALTER VIEW PROMOCION_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBRE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE

  FROM PROMOCION
  ORDER BY CLAVE
;