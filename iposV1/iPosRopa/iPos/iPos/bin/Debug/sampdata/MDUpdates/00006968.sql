CREATE OR ALTER VIEW GASTO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    CUENTAID,
    TIPOGASTOID,
    TIPOGASTOCLAVE,
    TIPOGASTONOMBRE)
AS
SELECT
    GASTO.ID,
    COALESCE(TRIM(GASTO.CLAVE), '') ||  COALESCE(TRIM(GASTO.NOMBRE), '') BUSQUEDASTRING,
    GASTO.ACTIVO,
    GASTO.CREADO,
    GASTO.CREADOPOR_USERID,
    GASTO.MODIFICADO,
    GASTO.MODIFICADOPOR_USERID,
    GASTO.CLAVE,
    GASTO.NOMBRE,
    GASTO.CUENTAID,
    GASTO.TIPOGASTOID,
    TIPOGASTO.CLAVE TIPOGASTOCLAVE,
    TIPOGASTO.NOMBRE TIPOGASTONOMBRE

  FROM GASTO
  LEFT JOIN TIPOGASTO ON GASTO.TIPOGASTOID = TIPOGASTO.ID
  ORDER BY CLAVE
;