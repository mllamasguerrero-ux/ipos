create or alter procedure KIT_PREPARETEMPTABLE
returns (
    ERRORCODE D_ERRORCODE)
as
BEGIN

   ERRORCODE = 0;

   DELETE FROM KITDEFTEMP;
   EXECUTE STATEMENT 'ALTER SEQUENCE SEQ_KITDEFTEMP RESTART WITH 0';
   
   DELETE FROM KITDOCTOTEMP;
   EXECUTE STATEMENT 'ALTER SEQUENCE SEQ_KITDOCTOTEMP RESTART WITH 0';


   ERRORCODE = 0;
   SUSPEND;
   
   WHEN ANY DO
   BEGIN
     ERRORCODE = 1076;
     SUSPEND;
   END
END