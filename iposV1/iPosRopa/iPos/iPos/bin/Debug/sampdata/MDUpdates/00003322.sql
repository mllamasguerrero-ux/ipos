CREATE OR ALTER VIEW TIPOTRANSACCION_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    SENTIDOINVENTARIO,
    SENTIDOPAGO)
AS
SELECT
    ID,
    COALESCE(TRIM(CLAVE), '') ||  COALESCE(TRIM(NOMBRE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLAVE,
    NOMBRE,
    SENTIDOINVENTARIO,
    SENTIDOPAGO

  FROM TIPOTRANSACCION
  ORDER BY CLAVE
;