CREATE OR ALTER TRIGGER REPLDOCTOPAGO_BI_05 FOR REPLDOCTOPAGO
ACTIVE BEFORE INSERT POSITION 5
AS
BEGIN
  IF ((NEW.REPLID IS NULL) OR (NEW.REPLID = 0)) THEN
    NEW.REPLID = GEN_ID(SEQ_REPLDOCTOPAGO, 1);
    
  IF (NEW.ACTIVO IS NULL) THEN
    NEW.ACTIVO = 'Y';

    
  IF(NEW.repltimecambio IS NULL) THEN
     NEW.repltimecambio = CURRENT_TIMESTAMP;
END