CREATE OR ALTER TRIGGER KARDEX_AI_20 FOR KARDEX
ACTIVE AFTER INSERT POSITION 20
AS
   DECLARE VARIABLE ERRORCODE D_ERRORCODE;
BEGIN
   SELECT ERRORCODE
   FROM KARDEX_INVENTARIO_UPDATE (
      NEW.DOCTOID,
      NEW.MOVTOID,
      NEW.PRODUCTOID,
      NEW.LOTE,
      NEW.FECHAVENCE,
      NEW.CANTIDAD,
      NEW.PRECIO,
      NEW.COSTO,
      NEW.ORIGENFISCALID,
      NEW.esapartado,
      NEW.ID,
      NEW.LOTEIMPORTADO
   ) INTO ERRORCODE;
END