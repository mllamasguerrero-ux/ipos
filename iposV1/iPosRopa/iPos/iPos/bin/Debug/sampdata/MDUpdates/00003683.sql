CREATE OR ALTER VIEW CLERKPAGOSERVICIO_VIEW(
    ID,
    BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLERKID,
    SUCURSALCLAVE)
AS
SELECT
    ID,
    COALESCE(TRIM(CLERKID), '') ||  COALESCE(TRIM(SUCURSALCLAVE), '') BUSQUEDASTRING,
    ACTIVO,
    CREADO,
    CREADOPOR_USERID,
    MODIFICADO,
    MODIFICADOPOR_USERID,
    CLERKID,
    SUCURSALCLAVE

  FROM CLERKPAGOSERVICIO
  ORDER BY CLERKID
;