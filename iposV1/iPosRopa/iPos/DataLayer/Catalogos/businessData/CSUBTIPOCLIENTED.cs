                auxPar.Value = oCSUBTIPOCLIENTE.IID;
                parts.Add(auxPar);

                FbParameter[] arParms = new FbParameter[parts.Count];
                parts.CopyTo(arParms, 0);

                string commandText = @"select 1 EXISTE
  from RDB$DATABASE
  where exists(
  select * from SUBTIPOCLIENTE where

  ID=@ID  
  );";






                if (st == null)
