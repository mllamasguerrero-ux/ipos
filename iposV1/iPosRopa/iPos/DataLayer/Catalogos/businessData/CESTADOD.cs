        [AutoComplete]
        public CESTADOBE AgregarESTADOD(CESTADOBE oCESTADO, FbTransaction st)
        {


            try
            {
                System.Collections.ArrayList parts = new ArrayList();
                FbParameter auxPar;

                auxPar = new FbParameter("@ACTIVO", FbDbType.Char);
                if (!(bool)oCESTADO.isnull["IACTIVO"])
                {
                    auxPar.Value = oCESTADO.IACTIVO;
                }
                else
                {
                    auxPar.Value = "S";
                }
                parts.Add(auxPar);


                auxPar = new FbParameter("@CREADOPOR_USERID", FbDbType.BigInt);
                if (!(bool)oCESTADO.isnull["ICREADOPOR_USERID"])
                {
                    auxPar.Value = oCESTADO.ICREADOPOR_USERID;
                }
                else
                {
                    auxPar.Value = System.DBNull.Value;
                }
                parts.Add(auxPar);



                auxPar = new FbParameter("@CLAVE", FbDbType.VarChar);
                if (!(bool)oCESTADO.isnull["ICLAVE"])
                {
                    auxPar.Value = oCESTADO.ICLAVE;
                }
                else
                {
                    auxPar.Value = System.DBNull.Value;
                }
                parts.Add(auxPar);


                auxPar = new FbParameter("@NOMBRE", FbDbType.VarChar);
                if (!(bool)oCESTADO.isnull["INOMBRE"])
                {
                    auxPar.Value = oCESTADO.INOMBRE;
                }
                else
                {
                    auxPar.Value = System.DBNull.Value;
                }
                parts.Add(auxPar);




                string commandText = @"
        INSERT INTO ESTADO
      (

ACTIVO,

CREADOPOR_USERID,

CLAVE,

NOMBRE
         )

Values (


@ACTIVO,

@CREADOPOR_USERID,

@CLAVE,

@NOMBRE
); ";

                FbParameter[] arParms = new FbParameter[parts.Count];
                parts.CopyTo(arParms, 0);

                if (st == null)
                    SqlHelper.ExecuteNonQuery(this.sCadenaConexion, CommandType.Text, commandText, arParms);
                else
                    SqlHelper.ExecuteNonQuery(st, CommandType.Text, commandText, arParms);






                return oCESTADO;

            }
            catch (Exception e)
            {
                this.iComentario = e.Message + e.StackTrace;
                return null;
            }
        }


